export function resolveLanguageCode(language) {
  const value = (language || "").toLowerCase();
  if (["spanish", "espanol", "español"].includes(value)) {
    return "es";
  }
  if (["persian", "farsi", "فارسی"].includes(value)) {
    return "fa";
  }
  return "en";
}

export function translate(language, key, fallback = "") {
  const code = resolveLanguageCode(language);
  const source = translations[code] || translations.en;
  const result = key.split(".").reduce((accumulator, segment) => accumulator?.[segment], source);
  return result || fallback || key;
}
